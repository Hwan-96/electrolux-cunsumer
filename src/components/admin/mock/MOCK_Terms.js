// 항목 검색 함수
export const searchTerms = (data, params) => {
  const { searchType, searchValue, tab } = params;
  
  return data.filter(item => {
    // 탭 필터링
    if (tab && item.tab !== tab) {
      return false;
    }
    
    // 검색어 필터링
    if (searchValue) {
      if (searchType === 'title') {
        return item.title.toLowerCase().includes(searchValue.toLowerCase());
      } else if (searchType === 'author') {
        return item.author.toLowerCase().includes(searchValue.toLowerCase());
      } else if (searchType === 'content') {
        return item.content.toLowerCase().includes(searchValue.toLowerCase());
      } else {
        // 전체 검색
        return (
          item.title.toLowerCase().includes(searchValue.toLowerCase()) ||
          item.author.toLowerCase().includes(searchValue.toLowerCase()) ||
          item.content.toLowerCase().includes(searchValue.toLowerCase())
        );
      }
    }
    
    return true;
  });
};

